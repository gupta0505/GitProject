package testPackage;

public class PrintVariables {

	public static void main(String[] args) {
		
		String name="kumar";
		System.out.println("Deepak "+name );
		
		
		String firstName="Julie ";
		String lastName="Gupta";
		String fullName=firstName +lastName;
		System.out.println(fullName);
		
		int x=3;
		int y=4;
		int z=x+y;
		System.out.println(z);
		
		
		int a=3, b=2, c=1;  // print multiple variable same type
		System.out.println(a+b+c);	
		
		
		int n,m,p;   // one value to multiple variable
		n=m=p=10;
		System.out.println(n+m+p);
		
		float f;
		f=4;
		System.out.print(f);
		
		
		
		
	}

}
